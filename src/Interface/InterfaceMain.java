    public java.sql.Date convertToDate(String tmp){
        java.sql.Date sqlDate = null;
        SimpleDateFormat sourceFormat = new SimpleDateFormat("yyyy-MM-dd");
        SimpleDateFormat targetFormat = new SimpleDateFormat("yyyy-MM-dd");
        try{
            java.util.Date dateText= sourceFormat.parse(tmp);
            String formattedDate = targetFormat.format(dateText);
            sqlDate = java.sql.Date.valueOf(formattedDate);
        }catch(ParseException e){
            System.out.println("Erreur de format de date : " + e.getMessage());
        }
        return sqlDate;
    }
